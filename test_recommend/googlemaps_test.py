import googlemaps
from dotenv import load_dotenv
import os
import pprint
import time
import json

genre = 'clothing_store' #絞るジャンル

key = os.getenv("GOOGLE_MAP_KEY") # 上記で作成したAPIキーを入れる
client = googlemaps.Client(key) #インスタンス生成

center_lat = 34.70605201690028
center_lng = 135.503858174402

results = []

directions = [
    (0.003, 0),        # 北
    (0, 0.003),        # 東
    (-0.003, 0),       # 南
    (0, -0.003)        # 西
]
for dlat, dlng in directions:
    lat = center_lat + dlat
    lng = center_lng + dlng
    place_result = client.places_nearby(location=(lat, lng), radius=300, keyword=genre)
    results.extend(place_result['results'])

# place_id を使って重複排除
unique_places = {}
for place in results:
    place_id = place.get("place_id")
    if place_id:
        unique_places[place_id] = place  # 辞書のキーにすることで重複が自動で消える

# 値だけ取り出してリストに
shop_list = list(unique_places.values())

# 重複なしの結果を新しいJSONに保存
with open("places_results.json", "w", encoding="utf-8") as f:
    json.dump(shop_list, f, ensure_ascii=False, indent=2)

